/// Create a feature package as a tar archive
async fn create_feature_package(
    feature_path: &Path,
    output_path: &Path,
    feature_id: &str,
) -> Result<(String, u64)> {
    use sha2::{Digest, Sha256};
    use std::fs::File;
    use std::io::{Read, Write};
    use tar::Builder;

    // Create tar file
    let tar_path = output_path.join(format!("{}.tar", feature_id));
    let tar_file = File::create(&tar_path)
        .with_context(|| format!("Failed to create tar file: {}", tar_path.display()))?;

    let mut ar = Builder::new(tar_file);

    // Add all files from feature directory to the tar
    for entry in std::fs::read_dir(feature_path).with_context(|| {
        format!(
            "Failed to read feature directory: {}",
            feature_path.display()
        )
    })? {
        let entry = entry?;
        let path = entry.path();
        let name = entry.file_name();

        if path.is_file() {
            ar.append_path_with_name(&path, &name)
                .with_context(|| format!("Failed to add file to tar: {}", path.display()))?;
        }
    }

    ar.finish().context("Failed to finalize tar archive")?;

    // Calculate digest and size
    let mut file = File::open(&tar_path).with_context(|| {
        format!(
            "Failed to open tar file for hashing: {}",
            tar_path.display()
        )
    })?;
    let mut hasher = Sha256::new();
    let mut buffer = vec![0; 8192];
    let mut total_size = 0u64;

    loop {
        let n = file.read(&mut buffer)?;
        if n == 0 {
            break;
        }
        hasher.update(&buffer[..n]);
        total_size += n as u64;
    }

    let digest = format!("sha256:{:x}", hasher.finalize());

    // Create manifest file
    let manifest_path = output_path.join(format!("{}-manifest.json", feature_id));
    let manifest = serde_json::json!({
        "id": feature_id,
        "digest": digest,
        "size": total_size,
        "tar_path": tar_path.display().to_string(),
    });

    let mut manifest_file = File::create(&manifest_path).with_context(|| {
        format!(
            "Failed to create manifest file: {}",
            manifest_path.display()
        )
    })?;
    manifest_file
        .write_all(serde_json::to_string_pretty(&manifest)?.as_bytes())
        .context("Failed to write manifest file")?;

    Ok((digest, total_size))
}

